import 'dart:io';

main(){

  print("Operator Increment");
  //Operator Increment digunakan untuk menambah variabel sebanyak satu angka, penempatan bisa diawal dan diakhir variabel
  //seperti ++a dan a++

  print("Prefix");

  var prefix = 5;

  print(++prefix);

  //nilai ekspresi var adalah var + 1. pertama tama kompilator menambah variabel dengan satu angka kemudian mencetak nilainya di console

}